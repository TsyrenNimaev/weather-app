export const windDirection = (deg: number) => {
  if (deg > 337.5) return 'Northerly';
  if (deg > 292.5) return 'North Westerly';
  if (deg > 247.5) return 'Westerly';
  if (deg > 202.5) return 'South Westerly';
  if (deg > 157.5) return 'Southerly';
  if (deg > 122.5) return 'South Easterly';
  if (deg > 67.5) return 'Easterly';
  if (deg > 22.5) return 'North Easterly';
  return 'Northerly';
};

export const getTime = (timezone: number) => {
  const localTime = new Date().getTime();
  const localOffset = new Date().getTimezoneOffset() * 60000;
  const currentUtcTime = localTime + localOffset;
  const cityOffset = currentUtcTime + 1000 * timezone;
  const cityTime = new Date(cityOffset).toTimeString().split(' ');

  return cityTime;
};

export const sunsetSunrise = (sunTime: number, timezone: number) => {
  const localOffset = new Date().getTimezoneOffset() * 60000;
  const currentUtcTime = sunTime * 1000 + localOffset;
  const currentTime = new Date(currentUtcTime + 1000 * timezone).toTimeString().split(' ');

  return currentTime;
};

